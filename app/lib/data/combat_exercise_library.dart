  /// Get exercises by sport category
  static List<CombatExercise> bySport(SportCategory sport) {
    return exercises.where((e) => e.sports.contains(sport)).toList();
  }

  /// Get exercises by equipment available
  static List<CombatExercise> byEquipment(
    List<EquipmentType> availableEquipment,
  ) {
    return exercises
        .where(
          (e) =>
              e.equipment.isEmpty ||
              e.equipment.any((eq) => availableEquipment.contains(eq)),
        )
        .toList();
  }

  /// Get exercises by intensity level range
  static List<CombatExercise> byIntensity(int min, int max) {
    return exercises
        .where((e) => e.intensityLevel >= min && e.intensityLevel <= max)
        .toList();
  }

  /// Get exercises by movement pattern
  static List<CombatExercise> byMovementPattern(MovementPattern pattern) {
    return exercises
        .where((e) => e.movementPatterns.contains(pattern))
        .toList();
  }

  /// Filter exercises for user profile
  static List<CombatExercise> forUserProfile(
    UserProfile profile, {
    SportCategory? sport,
    int limit = 100,
  }) {
    var filtered = exercises.where((exercise) {
      // Filter by fitness level
      final fitsLevel =
          profile.fitnessLevel.index >= exercise.minFitnessLevel.index &&
          profile.fitnessLevel.index <= exercise.maxFitnessLevel.index;

      // Filter by sport if specified
      final fitsSport = sport == null || exercise.sports.contains(sport);

      // Filter out exercises conflicting with limitations
      final limitations =
          profile.injuriesOrLimitations?.map((e) => e.toLowerCase()).toList() ??
          [];
      final conflictsWithLimitation = limitations.any((limitation) {
        return exercise.primaryMuscles.any(
              (muscle) => muscle.toLowerCase().contains(limitation),
            ) ||
            exercise.jointStress.keys.any(
              (joint) => joint.toLowerCase().contains(limitation),
            );
      });

      return fitsLevel && fitsSport && !conflictsWithLimitation;
    }).toList();

    // Sort by intensity
    filtered.sort((a, b) => a.intensityLevel.compareTo(b.intensityLevel));

    return filtered.take(limit).toList();
  }

  /// Search exercises by name or tags
  static List<CombatExercise> search(String query) {
    final lowerQuery = query.toLowerCase();
    return exercises
        .where(
          (e) =>
              e.name.toLowerCase().contains(lowerQuery) ||
              e.workoutTags.any(
                (tag) => tag.toLowerCase().contains(lowerQuery),
              ) ||
              e.skillFocus.any(
                (skill) => skill.toLowerCase().contains(lowerQuery),
              ),
        )
        .toList();
  }

  /// Get all exercises as legacy Exercise objects
  static List<Exercise> get asLegacyExercises {
    return exercises.map((e) => e.toLegacyExercise()).toList();
  }
