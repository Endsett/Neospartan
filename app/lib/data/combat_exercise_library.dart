/// New Exercise model with enhanced fields for expanded library
class CombatExercise {
  final String id;
  final String name;
  final ExerciseCategory category;
  final String youtubeId;
  final String targetMetaphor;
  final String instructions;
  final int intensityLevel; // 1-10
  final List<String> primaryMuscles;
  final Map<String, int> jointStress; // joint -> stress level 1-10
  final List<SportCategory> sports; // Which sports this exercise targets
  final List<EquipmentType> equipment;
  final List<MovementPattern> movementPatterns;
  final int estimatedDurationSeconds; // Per set/round
  final List<String> skillFocus; // Sport-specific skills
  final List<String> progressionSteps; // Easier variations
  final List<String> regressionSteps; // Harder variations
  final FitnessLevel minFitnessLevel;
  final FitnessLevel maxFitnessLevel;
  final List<String> workoutTags;
  final bool requiresPartner;
  final bool isSportSpecific;

  const CombatExercise({
    required this.id,
    required this.name,
    required this.category,
    required this.youtubeId,
    required this.targetMetaphor,
    required this.instructions,
    this.intensityLevel = 5,
    this.primaryMuscles = const [],
    this.jointStress = const {},
    this.sports = const [],
    this.equipment = const [],
    this.movementPatterns = const [],
    this.estimatedDurationSeconds = 60,
    this.skillFocus = const [],
    this.progressionSteps = const [],
    this.regressionSteps = const [],
    this.minFitnessLevel = FitnessLevel.beginner,
    this.maxFitnessLevel = FitnessLevel.advanced,
    this.workoutTags = const [],
    this.requiresPartner = false,
    this.isSportSpecific = false,
  });

  /// Convert to legacy Exercise for compatibility
  Exercise toLegacyExercise() {
    return Exercise(
      id: id,
      name: name,
      category: category,
      youtubeId: youtubeId,
      targetMetaphor: targetMetaphor,
      instructions: instructions,
      intensityLevel: intensityLevel,
      primaryMuscles: primaryMuscles,
      jointStress: jointStress,
      idealGoals: _mapSportsToTrainingGoals(),
      minFitnessLevel: minFitnessLevel,
      maxFitnessLevel: maxFitnessLevel,
      workoutTags: workoutTags,
    );
  }

  List<TrainingGoal> _mapSportsToTrainingGoals() {
    return sports.map((sport) {
      switch (sport) {
        case SportCategory.mma:
          return TrainingGoal.mma;
        case SportCategory.boxing:
          return TrainingGoal.boxing;
        case SportCategory.muayThai:
          return TrainingGoal.muayThai;
        case SportCategory.wrestling:
          return TrainingGoal.wrestling;
        case SportCategory.bjj:
          return TrainingGoal.bjj;
        case SportCategory.judo:
        case SportCategory.sambo:
        case SportCategory.grappling:
          return TrainingGoal.wrestling;
        case SportCategory.kickboxing:
        case SportCategory.striking:
        case SportCategory.karate:
        case SportCategory.taekwondo:
          return TrainingGoal.boxing;
        case SportCategory.clinch:
        case SportCategory.generalCombat:
          return TrainingGoal.generalCombat;
        case SportCategory.fightStrength:
        case SportCategory.strength:
          return TrainingGoal.strength;
        case SportCategory.fightConditioning:
        case SportCategory.fightPower:
        case SportCategory.fightEndurance:
        case SportCategory.conditioning:
          return TrainingGoal.conditioning;
        case SportCategory.general:
          return TrainingGoal.generalCombat;
      }
    }).toList();
  }
}

